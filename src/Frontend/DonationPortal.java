    private void DonateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DonateActionPerformed
        String namef = nameField.getText();
        String itemf = item.getText();
        String sectionf = section.getText();
        String strnum = qty.getText();
        int pro = program.getSelectedIndex();
        int categ = category.getSelectedIndex();
        int quantity;

        if (namef.isEmpty() || itemf.isEmpty() || sectionf.isEmpty() || strnum.isEmpty()) {
            errorMsg.setText("Please fill out all fields");
            return;
        }

        try {
            quantity = Integer.parseInt(strnum);
            if (quantity < 0) {
                errorMsg.setText("Quantity cannot be negative");
                return;
            }
        } catch (NumberFormatException e) {
            errorMsg.setText("Please input a valid number in quantity");
            return;
        }

        try (DBController dbc = new DBController()) {
            dbc.insertData(itemf, quantity, namef, categ, pro, sectionf);
            this.dispose();  
            new ThankyouPage().setVisible(true);
        } catch (SQLException ex) {
            errorMsg.setText("Failed to connect with the database");
        }
    }//GEN-LAST:event_DonateActionPerformed

